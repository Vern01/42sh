#include "libft.h"

void	ft_free_str_arr(char ***arr)
{
	int		i;

	if (*arr)
	{
		i = 0;
		while (arr[0][i] != NULL)
		{
			if (arr[0][i])
				free(arr[0][i]);
			arr[0][i] = NULL;
			i++;
		}
		free(*arr);
		*arr = NULL;
	}
}
